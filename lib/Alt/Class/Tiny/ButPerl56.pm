=head1 NAME

Alt::Class::Tiny::ButPerl56 - alternative distribution of Class::Tiny, with Perl 5.6 support

=head1 SYNOPSIS

   use Class::Tiny;

=head1 DESCRIPTION

This is an alternative distribution of Class::Tiny, allowing use on Perl 5.6.x. If you have had no problems installing Class::Tiny, then you do not need this module!

Hopefully an official release of Class::Tiny with support for Perl 5.6 will happen, and I'll be able to remove this abomination from the CPAN. :-)

Version 0.001 of Alt::Class::Tiny::ButPerl56 includes version 0.014 of Class::Tiny.

